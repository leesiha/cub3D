int **malloc_worldmap()
{
    int **map;
    int i;
    int j;

    i = 0;
    map = (int **)malloc(sizeof(int *) * mapWidth);
    while (i < mapWidth)
    {
        map[i] = (int *)malloc(sizeof(int) * mapHeight);
        j = 0;
        while (j < mapHeight)
        {
            map[i][j] = worldMap[i][j];
            j++;
        }
        i++;
    }
    return (map);
}

